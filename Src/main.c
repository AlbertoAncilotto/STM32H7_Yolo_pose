float smooth_keypoints(float* keypoints, float* prev_keypoints, float confidence, float prev_confidence, int num_keypoints){
  for (int i = 0; i < num_keypoints; i++) {
    float kpt_x = keypoints[2*i];
    float kpt_y = keypoints[2*i+1];
    float prev_kpt_x = prev_keypoints[2*i];
    float prev_kpt_y = prev_keypoints[2*i+1];

    keypoints[2*i] = (kpt_x*confidence + prev_kpt_x*prev_confidence)/(confidence+prev_confidence);
    keypoints[2*i+1] = (kpt_y*confidence + prev_kpt_y*prev_confidence)/(confidence+prev_confidence);
  }
  return (confidence+prev_confidence)/2;
}


